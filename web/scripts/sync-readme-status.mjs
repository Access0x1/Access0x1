#!/usr/bin/env node
/**
 * sync-readme-status.mjs — regenerate the README's CREATE3-mirror per-chain status table straight from
 * the committed broadcasts, so it can NEVER drift from on-chain reality (law #4). A chain counts as
 * "mirrored" iff its `broadcast/DeployAll.s.sol/<id>/run-latest.json` actually contains the canonical
 * mirror Router address (`Access0x1Router.proxy` from script/mirror-manifest.json). The table is spliced
 * between the MIRROR-STATUS markers in README.md — nothing outside the markers is touched.
 *
 * Run by `make sync` (alongside gen-deployments.mjs). `--check` exits 1 if the table is stale (for CI).
 *
 * Usage:
 *   node web/scripts/sync-readme-status.mjs           # rewrite the table from the broadcasts
 *   node web/scripts/sync-readme-status.mjs --check    # exit 1 if README is stale
 */
import { readFileSync, writeFileSync, existsSync, readdirSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { chainMeta, parseBroadcast } from './gen-deployments.mjs'

const HERE = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(HERE, '..', '..')
const BROADCAST_DIR = join(REPO_ROOT, 'broadcast', 'DeployAll.s.sol')
const MANIFEST = join(REPO_ROOT, 'script', 'mirror-manifest.json')
const README = join(REPO_ROOT, 'README.md')

const START = '<!-- MIRROR-STATUS:START (generated by `make sync` — do not edit by hand) -->'
const END = '<!-- MIRROR-STATUS:END -->'
/** Local/sim chain ids never surfaced in the product view. */
const LOCAL_CHAIN_IDS = new Set([31337])

/** The canonical mirror Router proxy address (lower-cased) — the marker of a mirrored chain. */
function mirrorRouter() {
  const addr = JSON.parse(readFileSync(MANIFEST, 'utf8')).contracts['Access0x1Router.proxy']
  if (typeof addr !== 'string') throw new Error('mirror-manifest.json: Access0x1Router.proxy missing')
  return addr.toLowerCase()
}

/**
 * Build the per-chain status table markdown from the broadcasts. A chain is mirrored iff its
 * run-latest.json contains the mirror Router address. Exported so a test can exercise the real logic.
 */
export function buildStatusTable(router = mirrorRouter()) {
  const chainIds = existsSync(BROADCAST_DIR)
    ? readdirSync(BROADCAST_DIR, { withFileTypes: true })
        .filter((d) => d.isDirectory() && /^\d+$/.test(d.name))
        .map((d) => Number(d.name))
        .filter((id) => !LOCAL_CHAIN_IDS.has(id))
        .sort((a, b) => a - b)
    : []
  const rows = chainIds
    .map((id) => {
      const file = join(BROADCAST_DIR, String(id), 'run-latest.json')
      if (!existsSync(file)) return null
      const mirrored = readFileSync(file, 'utf8').toLowerCase().includes(router)
      // Skip empty dry-run/sim records: keep a chain only if it carries the mirror set OR real
      // (legacy) deployments — same real-deployment bar as the web deployments view.
      if (!mirrored && parseBroadcast(id).length === 0) return null
      const { name } = chainMeta(id)
      return `| ${name} | \`${id}\` | ${mirrored ? '✅ mirror' : '⏳ pre-mirror'} |`
    })
    .filter(Boolean)
  return ['| Chain | Chain ID | CREATE3 mirror |', '| --- | --- | --- |', ...rows].join('\n')
}

function escapeRe(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

function main() {
  const checkOnly = process.argv.includes('--check')
  const block = `${START}\n\n${buildStatusTable()}\n\n${END}`
  const md = readFileSync(README, 'utf8')
  const re = new RegExp(`${escapeRe(START)}[\\s\\S]*?${escapeRe(END)}`)
  if (!re.test(md)) {
    console.error('README.md is missing the MIRROR-STATUS markers — add them once around the status table.')
    process.exit(1)
  }
  const next = md.replace(re, block)
  if (checkOnly) {
    if (next !== md) {
      console.error('README mirror-status table is stale — run `make sync`.')
      process.exit(1)
    }
    console.log('README mirror-status table up to date.')
    return
  }
  writeFileSync(README, next)
  const chains = block.split('\n').filter((l) => l.startsWith('| ') && !l.includes('---') && !l.includes('Chain ID'))
  console.log(`sync-readme-status: ${chains.length} chains -> README MIRROR-STATUS table`)
}

// Run only when invoked directly (never on import — the test imports buildStatusTable).
if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main()
}
