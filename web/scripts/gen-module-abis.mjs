#!/usr/bin/env node
/**
 * gen-module-abis.mjs — vendor the committed contract ABIs into ONE TS module
 * the browser reads, so the contract console can drive a read/write panel for
 * every deployed module without a cross-directory JSON import.
 *
 * Runs at build (and on demand) to turn the checked-in artifacts under the repo
 * root `abis/*.json` into a single committed-as-generated TS module:
 *
 *   web/lib/generated/module-abis.ts  — { <ContractName>: Abi } for every ABI in
 *                                        `abis/`, plus the ModuleName union and a
 *                                        MODULE_NAMES list.
 *
 * DOCTRINE: the ABI is never hand-edited here (it is the audited contract's own
 * interface); every value is read from `abis/*.json`, the SAME artifacts the
 * on-chain code was compiled from. The address side stays in `deployments.ts`
 * (broadcast ground truth) — this module carries ONLY interfaces, no addresses,
 * so an ABI can never smuggle in a guessed address (law #4 / guardrail #5).
 *
 * Usage:
 *   node scripts/gen-module-abis.mjs           # write the TS module
 *   node scripts/gen-module-abis.mjs --check    # exit 1 if the module is stale
 */
import { readFileSync, writeFileSync, existsSync, readdirSync, mkdirSync } from 'node:fs'
import { dirname, join, resolve, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const WEB_ROOT = resolve(HERE, '..')
const REPO_ROOT = resolve(WEB_ROOT, '..')
const ABIS_DIR = join(REPO_ROOT, 'abis')
const OUT_PATH = join(WEB_ROOT, 'lib', 'generated', 'module-abis.ts')

/**
 * Read every `abis/*.json` into a { name -> abiArray } map, sorted by name for
 * a stable, diff-friendly output. Each file is a bare ABI array (verified via
 * `Array.isArray`); a file that isn't an array is skipped with a warning rather
 * than emitting a malformed module.
 */
function loadAbis() {
  if (!existsSync(ABIS_DIR)) {
    throw new Error(`abis/ not found at ${ABIS_DIR}`)
  }
  const files = readdirSync(ABIS_DIR)
    .filter((f) => f.endsWith('.json'))
    .sort((a, b) => a.localeCompare(b))
  const out = {}
  for (const file of files) {
    const name = basename(file, '.json')
    const raw = readFileSync(join(ABIS_DIR, file), 'utf8')
    const parsed = JSON.parse(raw)
    // Support both a bare ABI array and a Foundry-style { abi: [...] } artifact.
    const abi = Array.isArray(parsed) ? parsed : parsed?.abi
    if (!Array.isArray(abi)) {
      console.warn(`gen-module-abis: skipping ${file} — not an ABI array`)
      continue
    }
    out[name] = abi
  }
  return out
}

/**
 * Render the generated TS module. Keys are emitted as literals (via `satisfies`)
 * so `keyof typeof MODULE_ABIS` is a precise union — a catalog entry that names a
 * module with no ABI is then a compile error, not a runtime surprise. Each ABI is
 * cast to viem's `Abi` (the panel drives it at runtime; it needs the shape, not
 * per-function literal inference), keeping `tsc` light on these large arrays.
 */
function renderModule(abis) {
  const names = Object.keys(abis)
  const entries = names
    .map((name) => `  ${JSON.stringify(name)}: (${JSON.stringify(abis[name])} as unknown) as Abi,`)
    .join('\n')
  return `// AUTO-GENERATED by scripts/gen-module-abis.mjs — DO NOT EDIT BY HAND.
// Regenerate with \`node scripts/gen-module-abis.mjs\` (runs on prebuild).
// Source: abis/*.json at the repo root (the audited contract interfaces).
//
// Interfaces ONLY — no addresses. The deployed address of each module resolves
// from lib/deployments.ts (broadcast ground truth), so an ABI can never carry a
// guessed address into the UI (law #4).
import type { Abi } from 'viem'

/** Every checked-in module ABI, keyed by contract name. */
export const MODULE_ABIS = {
${entries}
} satisfies Record<string, Abi>

/** The precise union of module names that have a committed ABI. */
export type ModuleName = keyof typeof MODULE_ABIS

/** Stable, sorted list of every module name with a committed ABI. */
export const MODULE_NAMES = Object.keys(MODULE_ABIS) as ModuleName[]
`
}

function main() {
  const checkOnly = process.argv.includes('--check')
  const abis = loadAbis()
  const ts = renderModule(abis)

  if (checkOnly) {
    const stale = !existsSync(OUT_PATH) || readFileSync(OUT_PATH, 'utf8') !== ts
    if (stale) {
      console.error('lib/generated/module-abis.ts is stale — run gen-module-abis.mjs')
      process.exit(1)
    }
    console.log('module ABIs up to date.')
    return
  }

  mkdirSync(dirname(OUT_PATH), { recursive: true })
  writeFileSync(OUT_PATH, ts)
  console.log(`gen-module-abis: ${Object.keys(abis).length} ABIs -> lib/generated/module-abis.ts`)
}

export { loadAbis, renderModule }

if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main()
}
