#!/usr/bin/env node
/**
 * gen-docs-corpus.mjs — vendor the repo-root `docs/*.md` corpus into ONE
 * committed TS module the server reads, so the documentation assistant has its
 * knowledge base available at RUNTIME without a filesystem read of `../docs`.
 *
 * Why a build-time codegen (not a runtime read): the web app ships as a Next.js
 * `output: 'standalone'` bundle, which traces ONLY the files an imported module
 * references — the repo-root `docs/` directory is NOT in the traced bundle, so a
 * server route that tried to `readFileSync('../docs/*.md')` at request time would
 * find nothing in production. Embedding the corpus as a plain TS import makes it
 * part of the traced bundle (guaranteed present) AND makes the build reproducible.
 * This mirrors how `gen-module-abis.mjs` vendors `abis/*.json` into a committed TS
 * module for the same "present in the bundle, no cross-dir read" reason.
 *
 * Turns the checked-in Markdown under the repo root `docs/*.md` into a single
 * committed-as-generated TS module:
 *
 *   web/lib/docs/corpus.generated.ts  — a typed `DOCS_CORPUS` array of
 *                                       { file, content } for every `docs/*.md`,
 *                                       sorted by filename for a stable diff.
 *
 * DOCTRINE: the content is never hand-edited here — it is copied verbatim from the
 * authored `docs/*.md`. The byte cap, per-file headers, and dropped-file reporting
 * live in `lib/docs/corpus.ts` (the loader), so this script stays a dumb, faithful
 * vendoring step and the cap policy is unit-testable in one place.
 *
 * Usage:
 *   node scripts/gen-docs-corpus.mjs           # write the TS module
 *   node scripts/gen-docs-corpus.mjs --check    # exit 1 if the module is stale
 */
import { readFileSync, writeFileSync, existsSync, readdirSync, mkdirSync } from 'node:fs'
import { dirname, join, resolve, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const WEB_ROOT = resolve(HERE, '..')
const REPO_ROOT = resolve(WEB_ROOT, '..')
const DOCS_DIR = join(REPO_ROOT, 'docs')
const OUT_PATH = join(WEB_ROOT, 'lib', 'docs', 'corpus.generated.ts')

/**
 * Read every `docs/*.md` into a `[{ file, content }]` array, sorted by filename
 * for a stable, diff-friendly output. `file` is the bare basename (e.g.
 * `FAQ.md`) so the loader can build a `docs/<file>` citation header; `content`
 * is the raw file text, copied verbatim.
 */
function loadDocs() {
  if (!existsSync(DOCS_DIR)) {
    throw new Error(`docs/ not found at ${DOCS_DIR}`)
  }
  const files = readdirSync(DOCS_DIR)
    .filter((f) => f.endsWith('.md'))
    .sort((a, b) => a.localeCompare(b))
  return files.map((file) => ({
    file: basename(file),
    content: readFileSync(join(DOCS_DIR, file), 'utf8'),
  }))
}

/**
 * Render the generated TS module. Each `content` is emitted via `JSON.stringify`
 * so every backtick, `${`, and backslash in the Markdown is safely escaped into a
 * valid TS string literal (the same escaping discipline `gen-module-abis.mjs`
 * relies on for ABI JSON). The array is typed as `readonly DocsCorpusEntry[]` —
 * NOT `as const` — so `tsc` treats each large body as a plain `string` instead of
 * inferring a giant literal type per file.
 */
function renderModule(docs) {
  const entries = docs
    .map((d) => `  { file: ${JSON.stringify(d.file)}, content: ${JSON.stringify(d.content)} },`)
    .join('\n')
  return `// AUTO-GENERATED by scripts/gen-docs-corpus.mjs — DO NOT EDIT BY HAND.
// Regenerate with \`node scripts/gen-docs-corpus.mjs\` (runs on prebuild).
// Source: docs/*.md at the repo root (the authored documentation corpus).
//
// Content ONLY — verbatim Markdown. The byte cap, per-file citation headers, and
// dropped-file reporting live in lib/docs/corpus.ts (the loader), so the cap
// policy is enforced and unit-tested in exactly one place.

/** One documentation file vendored into the corpus: its basename + raw Markdown. */
export interface DocsCorpusEntry {
  readonly file: string
  readonly content: string
}

/** Every checked-in \`docs/*.md\`, sorted by filename. */
export const DOCS_CORPUS: readonly DocsCorpusEntry[] = [
${entries}
]
`
}

function main() {
  const checkOnly = process.argv.includes('--check')
  const docs = loadDocs()
  const ts = renderModule(docs)

  if (checkOnly) {
    const stale = !existsSync(OUT_PATH) || readFileSync(OUT_PATH, 'utf8') !== ts
    if (stale) {
      console.error('lib/docs/corpus.generated.ts is stale — run gen-docs-corpus.mjs')
      process.exit(1)
    }
    console.log('docs corpus up to date.')
    return
  }

  mkdirSync(dirname(OUT_PATH), { recursive: true })
  writeFileSync(OUT_PATH, ts)
  console.log(`gen-docs-corpus: ${docs.length} docs -> lib/docs/corpus.generated.ts`)
}

export { loadDocs, renderModule }

if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main()
}
